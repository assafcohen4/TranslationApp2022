package com.app.translation.Model;

public class User {
    public String user_id;
    public String email;

    public String getID() {
        return user_id;
    }

    public void setID(String user_id) {
        this.user_id = user_id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
